;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : D8259.ASM
;     PROCESSOR : I8086
;     8259 TEST
;     Press IR2 button
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
INTA	EQU	10H
INTA2	EQU	INTA+2
	;
	ORG	1000H
	;
	CALL	INIT
	;
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11111111B
	OUT	PPIA,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	;
	MOV	AH,11110001B
	MOV	AL,AH
	OUT	PPIB,AL
	;
L2:	MOV	AL,00001100B
	OUT	INTA,AL
	IN	AL,INTA
	TEST	AL,10000000B
	JZ	L2
	;
	SHL	AH,1
	TEST	AH,00010000B
	JNZ	L1
	OR	AH,11110000B
	JMP	L3
	; LED out
L1:	MOV	AH,11110001B
L3:	MOV	AL,AH
	OUT	PPIB,AL
	;  EOI command
	MOV	AL,00100000B
	OUT	INTA,AL
	JMP	L2
	;
INIT	PROC	NEAR
	; ICW1
 	MOV	AL,00010011B
	OUT	INTA,AL
	;ICW2 interrupt vector
	MOV	AL,40H
	OUT	INTA2,AL
	;ICW4
	MOV	AL,00000001B
	OUT	INTA2,AL
	;interrupt mask
	MOV 	AL,11111011B
	OUT	INTA2,AL
	RET
INIT	ENDP
	;	
CODE	ENDS
	END

