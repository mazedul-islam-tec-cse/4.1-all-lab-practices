;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : MASM_0.ASM
;     PROCESSOR : I8086
;*****************************************
	CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
SEG_D	EQU	0000H
BIN_N	EQU	01101110B
ZERO_ASC EQU	30H
COUNT	EQU	0008H
	;
	ORG	1000H
	MOV	AX,SEG_D
	MOV	DS,AX
	;
	MOV	BL,BIN_N
	MOV	SI,OFFSET DATA
	MOV	CX,COUNT
	;
NEXT:	MOV	BYTE PTR[SI],ZERO_ASC
	TEST	BL,1
	JZ	ZERO
	INC	BYTE PTR[SI]
ZERO:	INC	SI
	RCR	BL,1	
	LOOP	NEXT
	;
	INT	3
	;
DATA	DB	8	DUP(?)
	;
CODE	ENDS
	END

