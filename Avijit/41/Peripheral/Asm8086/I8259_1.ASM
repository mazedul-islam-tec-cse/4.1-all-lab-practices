;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : D8259_1.ASM
;     PROCESSOR : I8086
;     8259 TEST
;     Press IR2 button
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
INTA	EQU	10H
INTA2	EQU	INTA+2
	;
INT_V	EQU	42H*4
	;
STACK	EQU	540H
	;
	ORG	1000H
	;
	XOR	BX,BX
	MOV	ES,BX
	MOV	DS,AX
	MOV	SS,BX
	MOV	SP,STACK
	;
	MOV	AX,OFFSET INT_SER
	MOV	BX,INT_V
	MOV	WORD PTR ES:[BX],AX
	;
	XOR	AX,AX
	MOV	WORD PTR ES:[BX+2],AX
	;
	CALL	INIT
	;
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	;
 	MOV	SI,OFFSET DATA
	MOV	AL,BYTE PTR CS:[SI]
	OUT	PPIA,AL
	;
	STI
L2:	NOP
	JMP	L2		
	;
	;
INT_SER:
	MOV	AL,BYTE PTR CS:[SI]
	CMP	AL,00H
	JNE	L3
	MOV	SI,OFFSET DATA
	JMP	INT_SER
	;
L3:	OUT	PPIA,AL
	INC	SI
	;  EOI command
	MOV	AL,00100000B
	OUT	INTA,AL
	;
	STI
	IRET	
	;
INIT	PROC	NEAR
	; ICW1
 	MOV	AL,00010011B
	OUT	INTA,AL
	;ICW2 interrupt vector
	MOV	AL,40H
	OUT	INTA2,AL
	;ICW4
	MOV	AL,00000001B
	OUT	INTA2,AL
	;interrupt mask
	MOV 	AL,11111011B
	OUT	INTA2,AL
	RET
INIT	ENDP
	;	
	;
DATA:	DB	11000000B
	DB	11111001B
	DB	10100100B
	DB	10110000B
	DB	10011001B
	DB	10010010B
	DB	10000010B
	DB	11111000B
	DB	10000000B
	DB	10010000B
	DB	00H
CODE	ENDS
	END

