;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : MASM_1.ASM
;     PROCESSOR : I8086
;*****************************************
	CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
SEG_D	EQU	0000H
STACK	EQU	0540H
	;
	ORG	1000H
	MOV	AX,SEG_D
	MOV	SS,AX
	MOV	DS,AX
	MOV	SP,STACK
	;
	MOV	CX,DATA_E-DATA1
	MOV	SI,OFFSET DATA1
	MOV	DI,OFFSET DATA2
	CALL	HASCII
	INT 	3
	;
HASCII  PROC   NEAR
	MOV	AL,BYTE PTR [SI]
	CMP	AL,0AH
	JC	NEXT
	ADD	AL,07H
NEXT:	ADD	AL,30H
	MOV	BYTE PTR [DI],AL
	INC	DI
	INC	SI
	LOOP	HASCII
	RET	
HASCII  ENDP
	;
	ORG	2000H
DATA1	DB	05H,0AH,09H,0BH,0FH,03H
DATA_E	DB	0
	;
	ORG	2100H
DATA2	DB	6	DUP(?)	
	;
CODE	ENDS
	END



